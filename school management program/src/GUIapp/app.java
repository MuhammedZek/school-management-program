        String ma = MN4.getText();
        if (uni.checkStudent(ma) == false) {
            JOptionPane.showMessageDialog(jPanel3, "ungultige Matrikelnummer oder existiert nicht einen Student mit diesem Matrikelnummer", "Fehler",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            Object o = combo.getSelectedItem();
            Student s1 = new Student(ma);
            int k = 0;
            for (k = 0; k < uni.getStudenten().size(); k++) {
                Student s2 = uni.getStudenten().elementAt(k);
                if (s2.getMatrikelnummer().compareTo(s1.getMatrikelnummer()) == 0) {
                    break;
                }
            }
            Student s3 = uni.getStudenten().elementAt(k);
            Vector<LV> l1 = uni.getStudenten().get(k).getAngemeldeteLV();
            boolean u = false;
            for (int r = 0; r < l1.size(); r++) {
                if (l1.get(r).getCode() == (String) o) {
                    u = true;
                    break;
                }
            }

            if (u) {
                JOptionPane.showMessageDialog(jPanel3, "Lehrveranstaltung schon existiert", "Fehler",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {

                DefaultTableModel dtm3 = (DefaultTableModel) SzLtable.getModel();
                dtm3.setRowCount(0);
                LV l = new LV((String) o);
                int i = 0;
                LV l2 = new LV();
                for (i = 0; i < uni.getLVs().size(); i++) {
                    l2 = uni.getLVs().elementAt(i);
                    if (l2.getCode().compareTo(l.getCode()) == 0) {
                        break;
                    }
                }

                uni.LVZuStudent(l2.getCode(), ma);

                Name4.setText(s3.toString());

                for (int j = 0; j < l1.size(); j++) {
                    String[] lv = new String[2];
                    lv[0] = l1.get(j).getCode();
                    lv[1] = l1.get(j).getName();
                    dtm3.addRow(lv);
                }
                LVfehlertext4.setText("Erfolgreich hinzugef√ºgt");
            }
        }
